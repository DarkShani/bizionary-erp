    regular_tier_count = models.IntegerField(default=0)
    new_signup_count = models.IntegerField(default=0)
    
    created_at = models.DateTimeField(auto_now_add=True)
    
    class Meta:
        db_table = 'customer_analytics_kpi'
        ordering = ['-date']
    
    def __str__(self):
        return f"KPIs for {self.date}"
