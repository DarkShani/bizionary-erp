    total_customers = models.IntegerField(default=0)
    total_customers_change = models.DecimalField(
        max_digits=5, 
        decimal_places=2, 
        default=0,
        help_text="Percentage change"
    )
    
    retention_rate = models.DecimalField(
        max_digits=5, 
        decimal_places=2, 
        default=0,
        validators=[MinValueValidator(0), MaxValueValidator(100)]
    )
    retention_rate_change = models.DecimalField(max_digits=5, decimal_places=2, default=0)
    
    avg_lifetime_value = models.DecimalField(max_digits=12, decimal_places=2, default=0)
    avg_lifetime_value_change = models.DecimalField(max_digits=5, decimal_places=2, default=0)
    
    churn_risk_percentage = models.DecimalField(
        max_digits=5, 
        decimal_places=2, 
        default=0,
        validators=[MinValueValidator(0), MaxValueValidator(100)]
    )
